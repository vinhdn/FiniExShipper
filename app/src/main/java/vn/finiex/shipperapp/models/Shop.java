public class Shop {

    private String name;
    private String giaPhepKinhDoanh;
    private String website;
    private String phone;
    private String maSoThue;
    private String address;
    private String linhVucKinhDoanh;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGiaPhepKinhDoanh() {
        return giaPhepKinhDoanh;
    }

    public void setGiaPhepKinhDoanh(String giaPhepKinhDoanh) {
        this.giaPhepKinhDoanh = giaPhepKinhDoanh;
    }

    public String getWebsite() {
        return website;
    }

    public void setWebsite(String website) {
        this.website = website;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getMaSoThue() {
        return maSoThue;
    }

    public void setMaSoThue(String maSoThue) {
        this.maSoThue = maSoThue;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getLinhVucKinhDoanh() {
        return linhVucKinhDoanh;
    }

    public void setLinhVucKinhDoanh(String linhVucKinhDoanh) {
        this.linhVucKinhDoanh = linhVucKinhDoanh;
    }
}
